public class operations {
    public static void main(String[] args){
        int x = 5; //101
        int leftShift = x << 3;  //10100
        System.out.println("Left Shift: " + leftShift);

        int rightShit = x >> 2;   //101 -> 10 
        System.out.println("Right Shift: " + rightShit);
    }
}
